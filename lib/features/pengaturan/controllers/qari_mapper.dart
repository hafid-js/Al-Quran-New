class QariMapper {
  static String getQariKey(int index) {
    switch (index) {
      case 0:
        return "01";
      case 1:
        return "02";
      case 3:
        return "03";
      case 4:
        return "05";
      case 5:
        return "06";
      default:
        return "01";
    }
  }
}
